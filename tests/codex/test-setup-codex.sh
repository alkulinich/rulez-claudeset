#!/usr/bin/env bash

MANAGED_BEGIN='<!-- rulez-claudeset:managed-begin -->'
MANAGED_END='<!-- rulez-claudeset:managed-end -->'
LEGACY_GENERATED_MARKER='<!-- generated by rulez-claudeset; do not edit directly -->'

manual_content_after_managed_block() {
  local path="$1"
  awk -v marker="$MANAGED_END" '
    found { print }
    $0 == marker { found=1 }
  ' "$path"
}

codex_forecast_prompt() {
  local path="$1"
  awk '
    $0 == "```text" && armed { in_prompt=1; next }
    in_prompt && $0 == "```" { exit }
    in_prompt { print }
    $0 == "Use this prompt as the complete task for the single `spawn_agent` call:" { armed=1 }
  ' "$path"
}

claude_forecast_prompt() {
  local path="$1"
  awk '
    $0 == "## Agent Prompt" { in_prompt=1; next }
    in_prompt {
      if (seen) { print }
      else if ($0 == "") { seen=1 }
    }
  ' "$path"
}

